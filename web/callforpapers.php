	           <h1>Call for Papers</h1>

               <p><strong>ABES Engineering College, Ghaziabad</strong>, one of the pioneer institutions in the field of Engineering, Research and Technology is organizing its 1st IEEE- International Conference on Computational Intelligence &amp; Communication technology; CICT-2015 This technical conference aims at providing a platform for industry and academia to discuss various emerging trends and innovations, share research results and new directions in upcoming areas.</p>

<p>CICT-2015 will provide a leading edge, scholarly forum for researchers, engineers, and students alike to share their state-of-the art research and developmental work in the broad areas of pervasive computing and communications. The conference will feature a diverse mixture of interactive forums, core technical sessions of high quality cutting-edge research articles; targeted workshops on exciting topics; live demonstrations of pervasive computing in action; insightful keynote speeches; panel discussions from domain experts and posters of emerging ideas and achieve the following:</p>

<ul style="list-style-type:square;margin:20px;margin-left:30px;">
	<li>To present the ongoing researches in the field and hence to foster research relations between the Universities and the industry.</li>
	<li>Give participants a review of the latest and upcoming trends in the next few years.</li>
	<li>Exposing the audience to the need for more development and research in the field and with it the growing need for IT security.</li>
	<li>Provide the delegates to share their new ideas and the application experiences face to face.</li>
</ul>

